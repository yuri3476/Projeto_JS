// VAR é uma forma mais antiga, deve-se optar por LET ou CONST
// CONST vem de constante, ou seja, o seu valor não pode ser alterado

let nome = "Matheus";

console.log(nome);
console.log(`O meu nome é ${nome}`);

let laranjas = 5;
console.log(laranjas * laranjas);

nome = "João";
console.log(nome);

laranjas = 456798;
console.log(laranjas);

laranjas = "Laranja";
console.log(laranjas);

let um = 1, dois = 2, tres = 3;
console.log(um + dois + tres);
